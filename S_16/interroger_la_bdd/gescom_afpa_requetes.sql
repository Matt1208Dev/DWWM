SELECT `pro_id`, `pro_ref`, `pro_name`, `cus_id`, `ord_id`, `ode_id` 
FROM `products` 
JOIN `orders_details` 
ON `pro_id` = `ode_pro_id` 
JOIN `orders` 
ON `ord_id` = `ode_ord_id` 
JOIN `customers` 
ON `cus_id` = `ord_cus_id` 
WHERE `cus_lastname` = 'Pikatchien';

